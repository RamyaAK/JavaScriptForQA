// calculating the area of a rectangle.

// function definition
function computeAreaOfRectanglewith(length,breadth){ // length,breadth are the fn parameters
    var area = length * breadth
    return area
}

console.log(computeAreaOfRectanglewith(10,34)) // 10,34 are the fn arguments.
console.log(computeAreaOfRectanglewith(2,4))
console.log(computeAreaOfRectanglewith(100,40))
console.log(computeAreaOfRectanglewith(10,20))
