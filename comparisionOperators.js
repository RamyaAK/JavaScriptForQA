// Comparision Operaators in JS
/*
==, ===, <, >, <=, >=, !=
*/

var a = 1
var b = 0
var c ="1"

console.log(a==c) // shallow comparision without type only value is checked.
console.log(a===c) // strict type check 
console.log(a==b) //Equality comparision 
console.log(a!=c) // Not Equal
console.log(a<c) // Greater than
console.log(a>c) // Lesser than
console.log(a<=b) // Greater than or Equal to
console.log(a>=b) // Lesser than or Equal to