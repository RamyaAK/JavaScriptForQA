// Math Operators:  +,-,*,/,%,**,++,--

var a=10
var b=5
//Addition
console.log(a+b)
//Subtraction
console.log(a-b)
//Multiplication
console.log(a*b)
//Division
console.log(a/b)
//modulo or remainder
console.log(a%b)
//a to the Power of b
console.log(a**b)
//Post Increment Operator
console.log(a++)
//Post decrement Operator
console.log(b--)

a=10
b=5
//Pre Increment Operator
console.log(++a)
//Pre decrement Operator
console.log(--b)

